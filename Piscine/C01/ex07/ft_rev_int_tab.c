void	ft_rev_int_tab(int *tab, int size)
{
	int	c;
	int	temp;

	c = 0;
	while (c < size / 2)
	{
		temp = *(tab + c);
		*(tab + c) = *(tab + (size -1 - c));
		*(tab + (size -1 - c)) = temp;
		c++;
	}
}
