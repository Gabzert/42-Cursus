int	ft_iterative_factorial(int nb)
{	
	int	ris;

	if (nb >= 0)
	{
		ris = 1;
		while (nb > 0)
		{
			ris = ris * nb;
			nb--;
		}
		return (ris);
	}
	return (0);
}
