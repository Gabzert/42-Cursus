int	ft_sqrt(int nb)
{
	int	c;

	c = 1;
	if (nb > 0)
	{
		while ((nb / c) != c || (nb % c) != 0)
		{
			c++;
			if (c == nb)
				return (0);
			else if (c >= 46341)
				return (0);
		}
		return (c);
	}
	return (0);
}
